# ── 写入单条条目 ───────────────────────────────────────────────────
def create_entry(db_id, entry):
    def rt(text):
        return [{"type": "text", "text": {"content": text or ""}}]
    def ms(values):
        return [{"name": v} for v in (values or [])]

    props = {
        "条目名称":    {"title": rt(entry.get("条目名称", ""))},
        "白话字／拼音": {"rich_text": rt(entry.get("白话字／拼音", ""))},
        "子类":        {"rich_text": rt(entry.get("子类", ""))},
        "时间／时节":  {"rich_text": rt(entry.get("时间／时节", ""))},
        "简单介绍":    {"rich_text": rt(entry.get("简单介绍", ""))},
        "所需物品":    {"rich_text": rt(entry.get("所需物品", ""))},
        "操作步骤":    {"rich_text": rt(entry.get("操作步骤", ""))},
        "禁忌与避讳":  {"rich_text": rt(entry.get("禁忌与避讳", ""))},
        "背后故事":    {"rich_text": rt(entry.get("背后故事", ""))},
        "地域差异":    {"rich_text": rt(entry.get("地域差异", ""))},
    }
    if entry.get("大类"):
        props["大类"] = {"select": {"name": entry["大类"]}}
    if entry.get("地域层级"):
        props["地域层级"] = {"select": {"name": entry["地域层级"]}}
    if entry.get("完成状态"):
        props["完成状态"] = {"select": {"name": entry["完成状态"]}}
    if entry.get("可信度"):
        props["可信度"] = {"select": {"name": entry["可信度"]}}
    if entry.get("适用地区"):
        props["适用地区"] = {"multi_select": ms(entry["适用地区"])}

    payload = {
        "parent": {"database_id": db_id},
        "properties": props,
    }
    notion_request("POST", "pages", payload)

